                        text = "Rp. " + formatNumber(hitung3) + " / Panen";
                    }
                };
                document.getElementById("return-Invest").innerHTML = text;
            }
            function formatNumber (num) {
                return num.toString().replace(/(\d)(?=(\d{3})+(?!\d))/g, "$1,")
